  <tr>
    <td><p align="left">The  SM chemicals and SM minilab equipment range is designed to ensure that the  management and handling of the photoprocessing chemicals is simple, safe and  easy.</p>
      <p align="left">The  chemicals for replenishment are supplied in clearly marked cartridges that are  easily coupled to the minilab with no chemical mixing involved. This eliminates  the potential for mixing errors, which can cause the incorrect processing of  customer films or costly rework of prints. Staff can concentrate on processing  prints rather than mixing chemicals.</p>
